const mongoose = require("mongoose");
const bcrypt = require("bcrypt");

const skiStudentSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  { collection: "ski-students" }
);

// Middleware - Hash
skiStudentSchema.pre("save", async function (next) {
  if (this.isModified("password")) {
    this.password = await bcrypt.hash(this.password, 10);
  }
  next();
});

module.exports = mongoose.model("SkiStudent", skiStudentSchema);
